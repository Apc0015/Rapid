class TestUserDatabase:
    """Verify that users are persisted to and read from SQLite (Task 2)."""

    @pytest.fixture(autouse=True)
    def _setup(self, tmp_path, monkeypatch):
        import infrastructure.user_registry as ur
        self.tmp_path = tmp_path
        # Redirect USERS_YAML → tmp so DB also lives in tmp
        self.users_yaml = tmp_path / "users.yaml"
        self.users_yaml.write_text(yaml.dump({}))
        monkeypatch.setattr(ur, "USERS_YAML", self.users_yaml)
        self.ur = ur

    def _make_user(self, login_key: str, password: str = "Pass1234!", role: str = "employee") -> dict:
        return {
            "name": login_key.capitalize(),
            "role": role,
            "email": f"{login_key}@example.com",
            "password_hash": _make_hash(password),
            "permitted_departments": ["hr"],
            "db_mode_enabled": False,
        }

    def test_init_creates_table(self):
        """init_user_db() creates the users table without error."""
        self.ur.init_user_db()
        conn = self.ur._db_connect()
        rows = conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name='users'"
        ).fetchall()
        conn.close()
        assert len(rows) == 1

    def test_seed_from_yaml_on_empty_db(self):
        """On empty DB, init_user_db() seeds users from users.yaml."""
        users = {"alice": self._make_user("alice")}
        self.users_yaml.write_text(yaml.dump(users))
        self.ur.init_user_db()
        loaded = self.ur.load_users()
        assert "alice" in loaded

    def test_seed_does_not_overwrite_existing_db(self):
        """init_user_db() is a no-op when DB already has users."""
        # Write "alice" directly to DB
        self.ur._db_save_all_users({"alice": self._make_user("alice")})
        # YAML has "bob" — should NOT overwrite DB content
        self.users_yaml.write_text(yaml.dump({"bob": self._make_user("bob")}))
        self.ur.init_user_db()
        loaded = self.ur.load_users()
        assert "alice" in loaded
        assert "bob" not in loaded  # YAML seeding skipped

    def test_load_users_returns_db_users_when_populated(self):
        """load_users() returns from DB when DB has records."""
        user_data = self._make_user("carol")
        self.ur._db_save_all_users({"carol": user_data})
        loaded = self.ur.load_users()
        assert "carol" in loaded
        assert loaded["carol"]["role"] == "employee"

    def test_save_users_persists_to_db_and_yaml(self):
        """_save_users() writes to BOTH DB and the YAML backup."""
        users = {"dave": self._make_user("dave")}
        self.ur._save_users(users)
        # DB check
        db_users = self.ur._db_load_all_users()
        assert "dave" in db_users
        # YAML check (write-through backup)
        yaml_users = yaml.safe_load(self.users_yaml.read_text()) or {}
        assert "dave" in yaml_users

    def test_permitted_departments_round_trips_correctly(self):
        """List fields survive DB round-trip as Python lists (not JSON strings)."""
        user_data = self._make_user("eve")
        user_data["permitted_departments"] = ["hr", "finance", "legal"]
        self.ur._db_save_all_users({"eve": user_data})
        loaded = self.ur.load_users()
        assert loaded["eve"]["permitted_departments"] == ["hr", "finance", "legal"]

    def test_tenant_id_round_trips_through_the_database(self):
        """Tenant identity must survive DB-backed user storage and JWT login seeding."""
        user_data = self._make_user("tenant_user")
        user_data["tenant_id"] = "tenant-alpha"
        self.ur._db_save_all_users({"tenant_user": user_data})

        loaded = self.ur.load_users()
        assert loaded["tenant_user"]["tenant_id"] == "tenant-alpha"

    def test_load_falls_back_to_yaml_when_db_empty(self):
        """When DB has no records, load_users() falls back to YAML."""
        users = {"fallback_user": self._make_user("fallback_user")}
        self.users_yaml.write_text(yaml.dump(users))
        # Don't populate DB — load should fall back to YAML
        loaded = self.ur.load_users()
        assert "fallback_user" in loaded

    def test_change_password_updates_db(self):
        """change_password() updates the user record in the DB (via _save_users)."""
        users = {"frank": self._make_user("frank", password="OldPass1!")}
        self.ur._save_users(users)
        self.ur.change_password("frank", "OldPass1!", "NewPass1!")
        db_users = self.ur._db_load_all_users()
        # New hash must verify the new password
        from infrastructure.user_registry import verify_password
        assert verify_password("NewPass1!", db_users["frank"]["password_hash"])
        assert not verify_password("OldPass1!", db_users["frank"]["password_hash"])
