class TestVerifyPassword:
    """T-29 through T-34 — core password verification logic."""

    def setup_method(self):
        from infrastructure.user_registry import hash_password, verify_password
        self.hash_password  = hash_password
        self.verify_password = verify_password

    def test_T29_correct_bcrypt_password_returns_true(self):
        """T-29: Correct bcrypt password is verified as True."""
        stored = self.hash_password("CorrectPass1")
        assert self.verify_password("CorrectPass1", stored) is True

    def test_T30_wrong_password_returns_false(self):
        """T-30: Wrong password returns False — must NOT pass auth."""
        stored = self.hash_password("CorrectPass1")
        assert self.verify_password("WrongPassword", stored) is False

    def test_T31_legacy_pbkdf2_hash_verified(self):
        """T-31: Legacy PBKDF2 hashes (salt$key format) are still accepted."""
        import hashlib, secrets
        salt = secrets.token_hex(16)
        key  = hashlib.pbkdf2_hmac("sha256", "LegacyPass".encode(), salt.encode(), 260_000).hex()
        stored = f"{salt}${key}"
        assert self.verify_password("LegacyPass", stored) is True
        assert self.verify_password("WrongPass",  stored) is False

    def test_T32_corrupt_hash_returns_false_not_crash(self):
        """T-32: Corrupt/malformed hash returns False — must not raise."""
        assert self.verify_password("anypassword", "notahash") is False
        assert self.verify_password("anypassword", "") is False
        assert self.verify_password("anypassword", "nodollar") is False

    def test_T33_empty_password_returns_false(self):
        """T-33: Empty string password must not authenticate."""
        stored = self.hash_password("RealPass123")
        assert self.verify_password("", stored) is False

    def test_T34_unicode_password_consistent(self):
        """T-34: Unicode passwords hash and verify consistently."""
        pw     = "pässwörd123"
        stored = self.hash_password(pw)
        assert self.verify_password(pw, stored) is True
        assert self.verify_password("passwrd123", stored) is False  # ASCII variant rejected


