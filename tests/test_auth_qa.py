class TestLogoutEndpoint:
    """T-15 through T-19."""

    @pytest.fixture(autouse=True)
    def _setup(self, tmp_path, monkeypatch):
        import infrastructure.jwt_manager as jm
        db_path = str(tmp_path / "jwt.db")
        monkeypatch.setattr(jm, "DB_PATH", db_path)
        jm._jwt_manager = None
        self.manager = jm.get_jwt_manager()

        from fastapi import FastAPI
        from routers.auth import router
        from slowapi import Limiter
        from slowapi.util import get_remote_address
        from slowapi.middleware import SlowAPIMiddleware
        app = FastAPI()
        app.include_router(router)
        app.add_middleware(SlowAPIMiddleware)
        app.state.limiter = Limiter(key_func=get_remote_address)

        from fastapi.testclient import TestClient
        self.client = TestClient(app, raise_server_exceptions=False)

        # Create an access token for authenticated calls
        self.access_token = self.manager.create_access_token("testuser", "employee", ["hr"])

    def _auth_headers(self):
        return {"Authorization": f"Bearer {self.access_token}"}

    def test_T15_logout_revokes_refresh_token(self):
        """T-15: Logout returns 200 and revokes the refresh token."""
        refresh = self.manager.create_refresh_token("testuser")
        resp = self.client.post(
            "/auth/logout",
            json={"refresh_token": refresh},
            headers=self._auth_headers(),
        )
        assert resp.status_code == 200
        # Verify token is now revoked
        with pytest.raises(ValueError, match="revoked"):
            self.manager.verify_refresh_token(refresh)

    def test_T16_refresh_rejected_after_logout(self):
        """T-16: CRITICAL — refresh token submitted to /auth/refresh after logout must fail."""
        users_content = yaml.dump({"testuser": {"role": "employee", "permitted_departments": ["hr"], "password_hash": _make_hash("x")}})
        refresh = self.manager.create_refresh_token("testuser")
        # Logout
        self.client.post("/auth/logout", json={"refresh_token": refresh}, headers=self._auth_headers())
        # Try to refresh
        with patch("pathlib.Path.exists", return_value=True), \
             patch("pathlib.Path.read_text", return_value=users_content):
            resp = self.client.post("/auth/refresh", json={"refresh_token": refresh})
        assert resp.status_code == 401

    def test_T17_logout_all_revokes_all_tokens(self):
        """T-17: /auth/logout-all revokes all refresh tokens for user."""
        t1 = self.manager.create_refresh_token("testuser")
        t2 = self.manager.create_refresh_token("testuser")
        t3 = self.manager.create_refresh_token("testuser")
        resp = self.client.post("/auth/logout-all", headers=self._auth_headers())
        assert resp.status_code == 200
        assert "3" in resp.json()["message"]  # "Logged out from 3 device(s)"
        for t in (t1, t2, t3):
            with pytest.raises(ValueError):
                self.manager.verify_refresh_token(t)

    def test_T18_cannot_revoke_another_users_token(self):
        """T-18 FIXED: Cross-user refresh-token revocation is blocked with 403.

        The logout endpoint decodes the submitted refresh token (signature not
        verified to handle expired tokens gracefully) and compares its 'sub' to
        the authenticated user's 'sub'.  A mismatch → 403 Forbidden.
        User B's token must remain valid after the rejected attempt.
        """
        # Create user B's refresh token (user B != testuser)
        other_refresh = self.manager.create_refresh_token("otheruser")

        # User A (testuser) attempts to revoke user B's token
        resp = self.client.post(
            "/auth/logout",
            json={"refresh_token": other_refresh},
            headers=self._auth_headers(),  # testuser's access token
        )
        # Ownership check must reject this with 403
        assert resp.status_code == 403, (
            "logout must return 403 when the refresh token's sub "
            "does not match the authenticated user's sub"
        )
        assert "belong to you" in resp.json().get("detail", "").lower()

        # User B's token must still be valid — the revocation was refused
        payload = self.manager.verify_refresh_token(other_refresh)
        assert payload["sub"] == "otheruser", "User B's token was wrongly revoked"

    def test_T19_logout_already_revoked_token_idempotent(self):
        """T-19: Logging out with an already-revoked token returns 200 (idempotent)."""
        refresh = self.manager.create_refresh_token("testuser")
        self.manager.revoke_refresh_token(refresh)
        resp = self.client.post("/auth/logout", json={"refresh_token": refresh}, headers=self._auth_headers())
        assert resp.status_code == 200


